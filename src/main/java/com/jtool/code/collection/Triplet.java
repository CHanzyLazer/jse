package com.jtool.code.collection;

/**
 * @author liqa
 * <p> STL style Triplet,
 * simple and more powerful </p>
 */
public class Triplet<A, B, C> {
    public A a;
    public B b;
    public C c;
    
    public Triplet(A a, B b, C c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
}
